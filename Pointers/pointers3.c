#include<stdio.h>

//pointer arithmetic(addition)

int main()
{
	int a[5] = {0, 1, -1, 10, 5};
	int *p = &a[0]; //you can also write as int *p = a;
	printf("%d\n", *p);

	p = p + 2;
	printf("%d\n", *p);

	p = p + 1;
	printf("%d\n", *p);

	//this will print a garbage value because it exceeds the elements in the array
	p = p + 5;
	printf("%d\n", *p);

	//this will dereference the value of p to 2
	*p = 2;
	printf("%d\n", *p);

}














































































































































































































































































































